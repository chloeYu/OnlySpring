package only.model;

public class HashTag {
	private String hName;
	private int hid;
	private int pid;

	public String gethName() {
		return hName;
	}

	public void setTagName(String hName) {
		this.hName = hName;
	}

	public int gethid() {
		return hid;
	}

	public void sethid(int hid) {
		this.hid = hid;
	}

	public int getpid() {
		return pid;
	}

	public void setpid(int pid) {
		this.pid = pid;
	}

}
